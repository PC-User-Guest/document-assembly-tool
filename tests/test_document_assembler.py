def test_find_placeholders():
    assembler = DocumentAssembler(
        data_source=str(SAMPLE_DATA_WORD),
        template_path=str(SAMPLE_TEMPLATE),
        output_path=str(OUTPUT_TEST),
        log_level="ERROR"
    )
    doc = Document(SAMPLE_TEMPLATE)
    placeholders = assembler.find_placeholders(doc)
    # We expect three placeholders: {{client_name}}, {{project_title}}, {{budget}}
    assert len(placeholders) == 3
    field_names = [m.group('field_name') for _, m in placeholders]
    assert set(field_names) == {"client_name", "project_title", "budget"}


def test_insert_data():
    assembler = DocumentAssembler(
        data_source=str(SAMPLE_DATA_WORD),
        template_path=str(SAMPLE_TEMPLATE),
        output_path=str(OUTPUT_TEST),
        data_type="word",
        log_level="ERROR"
    )
    data = assembler.load_data()
    assembler.insert_data(data)
    assert OUTPUT_TEST.exists()
    doc = Document(OUTPUT_TEST)
    full_text = "\n".join([p.text for p in doc.paragraphs])
    # Placeholders should be gone
    assert "{{client_name}}" not in full_text
    assert "{{project_title}}" not in full_text
    assert "{{budget}}" not in full_text
    # Check that data appears
    assert "Acme Corp" in full_text
    assert "Enterprise Platform" in full_text
    assert "$150,000" in full_text

    # Validate that formatting is preserved for inline placeholder replacements.
    bold_runs = [r for p in doc.paragraphs for r in p.runs if r.bold]
    assert any("Acme Corp" in r.text for r in bold_runs)

    # Clean up
    OUTPUT_TEST.unlink(missing_ok=True)
