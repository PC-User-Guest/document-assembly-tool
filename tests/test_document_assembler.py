"""
Unit tests for the Document Assembly Tool.

These tests use the sample files generated by generate_fixtures.py.
"""

import pytest
import os
import json
import csv
from pathlib import Path
from docx import Document
from src.document_assembler import DocumentAssembler

FIXTURE_DIR = Path(__file__).parent / "fixtures"
SAMPLE_DATA_WORD = FIXTURE_DIR / "sample_data.docx"
SAMPLE_DATA_CSV = FIXTURE_DIR / "sample_data.csv"
SAMPLE_DATA_JSON = FIXTURE_DIR / "sample_data.json"
SAMPLE_TEMPLATE = FIXTURE_DIR / "sample_template.docx"
OUTPUT_TEST = FIXTURE_DIR / "test_output.docx"


@pytest.fixture(scope="module", autouse=True)
def ensure_fixtures():
    """Generate fixtures if missing."""
    if not all(f.exists() for f in [SAMPLE_DATA_WORD, SAMPLE_DATA_CSV, SAMPLE_DATA_JSON, SAMPLE_TEMPLATE]):
        import subprocess
        subprocess.run(["python", str(FIXTURE_DIR.parent / "generate_fixtures.py")], check=True)
    yield


def test_load_data_word():
    assembler = DocumentAssembler(
        data_source=str(SAMPLE_DATA_WORD),
        template_path=str(SAMPLE_TEMPLATE),
        output_path=str(OUTPUT_TEST),
        data_type="word",
        log_level="ERROR"
    )
    data = assembler._load_data_from_word()
    assert "client_name" in data
    assert "project_title" in data
    assert "budget" in data
    # Check that value is list of paragraph dicts
    assert isinstance(data["client_name"], list)
    assert len(data["client_name"]) >= 1
    assert "runs" in data["client_name"][0]


